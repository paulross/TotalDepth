class MockStreamRead(object):
    def __init__(self, b):
        self._b = b
        self._p = 0
        
    def read(self, n=1):
        """Get n bytes and increment the index, if n is negative then return all the remaining bytes."""
        if n < 0:
            myP = self._p
            self._p = len(self._b)
            return self._b[myP:]
        self._p += n
        return self._b[self._p-n:self._p]
        
